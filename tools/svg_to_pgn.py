################################################################################
# Convert a svg image to pgn format
################################################################################
import cairosvg

################################################################################
################################################################################


def main():
    print("Insert input file path:")
    input_url: str = input()
    print("Insert output file path:")
    output_url: str = input()
    cairosvg.svg2png(url=input_url, write_to=output_url)


################################################################################
################################################################################


main()
